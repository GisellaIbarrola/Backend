const mongoose = require('mongoose')

const usersCollection = 'usersLogin'

const UserSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
})

const UsersModel = mongoose.model(usersCollection, UserSchema)

module.exports = UsersModel
